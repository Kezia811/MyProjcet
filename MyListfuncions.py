# Demosntração de funções em listas
NÚMEROS = [7, 2, 9, 6, 5, 0, 3, 8, 1, 4]
PALAVRAS = ["olá", "alô", "hei", "uau", "ops"]

print("Quantas variáveis possui:")
print("Números:", len (NÚMEROS))
print("Palavras:", len (PALAVRAS))

print("Vamos reordenar estas listas?")
print(sorted (NÚMEROS))
print(sorted (PALAVRAS))

print("O somatório de números.", sum (NÚMEROS))
print("Qual é o maior valor?", max (NÚMEROS))
print("Qual é a primeira palavra?", min (NÚMEROS))
